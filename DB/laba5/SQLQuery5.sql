(SELECT AVG(NOTE) FROM PROGRESS
WHERE SUBJECT LIKE 'ОАиП') [ОАиП],
(SELECT AVG(NOTE) FROM PROGRESS
WHERE SUBJECT LIKE 'СУБД') [СУБД],
(SELECT AVG(NOTE) FROM PROGRESS
WHERE SUBJECT LIKE 'БД') [БД]
