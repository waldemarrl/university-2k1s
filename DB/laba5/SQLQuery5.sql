SELECT AUDITORIUM_TYPE AS [Тип аудитории],
AUDITORIUM_CAPACITY AS Вместимость
FROM AUDITORIUM a
WHERE AUDITORIUM_TYPE = (
SELECT TOP(1) AUDITORIUM_TYPE 
FROM AUDITORIUM aa
WHERE aa.AUDITORIUM_TYPE = a.AUDITORIUM_TYPE)
ORDER BY AUDITORIUM_CAPACITY desc
