SELECT FACULTY.FACULTY_NAME AS Факультет, 
PULPIT.PULPIT_NAME AS Кафедра
FROM FACULTY, PULPIT
WHERE PULPIT.FACULTY = FACULTY.FACULTY
AND FACULTY.FACULTY IN (
SELECT PROFESSION.FACULTY
FROM PROFESSION
WHERE (PROFESSION_NAME LIKE '%технологи%'))
